interface I18nContextType {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: string, fallback?: string) => string;
}

const I18nContext = createContext<I18nContextType | undefined>(undefined);

export const I18nProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  const [language, setLanguage] = useState<Language>(() => {
    const saved = localStorage.getItem('vendorLanguage');
    if (saved === 'de' || saved === 'tr') return saved;
    return 'en';
  });

  const value = useMemo(
    () => ({
      language,
      setLanguage: (next: Language) => {
        localStorage.setItem('vendorLanguage', next);
        setLanguage(next);
      },
      t: (key: string, fallback?: string) =>
        extendedTranslations[language]?.[key] ||
        (translations[language] as Record<string, string>)[key] ||
        extendedTranslations.en?.[key] ||
        (translations.en as Record<string, string>)[key] ||
        fallback ||
        key,
    }),
    [language]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const context = useContext(I18nContext);
  if (!context) throw new Error('useI18n must be used within I18nProvider');
  return context;
};
